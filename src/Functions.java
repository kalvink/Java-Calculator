
public class Functions {

	private static int total;
	
	
	

	static int addition(int one, int two) {
		total = one + two;
		return total;

	}

	static int subtract(int one, int two) {
		total = one - two;
		return total;

	}

	static int multiplication(int one, int two) {
		total = one * two;
		return total;

	}

	static int division(int one, int two) {
		total = one / two;
		return total;

	}
	
	
	
}
