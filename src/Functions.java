
public class Functions {

	private int total;
	
	
	

	private int addition(int one, int two) {
		total = one + two;
		return total;

	}

	private int subtract(int one, int two) {
		total = one - two;
		return total;

	}

	private int multiplication(int one, int two) {
		total = one * two;
		return total;

	}

	private int division(int one, int two) {
		total = one / two;
		return total;

	}
	
	
	
}
